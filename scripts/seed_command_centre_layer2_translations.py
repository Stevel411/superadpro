added_total = 0
for lang, new_keys in TRANSLATIONS.items():
    path = LOCALES_DIR / f'{lang}.json'
    if not path.exists():
        print(f'! {lang}.json missing, skipping')
        continue
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    if 'commandCentre' not in data:
        print(f'! {lang}.json has no commandCentre namespace yet, skipping')
        continue
    cc = data['commandCentre']
    added_for_lang = 0
    for key, value in new_keys.items():
        if key not in cc:
            cc[key] = value
            added_for_lang += 1
    if added_for_lang > 0:
        with open(path, 'w', encoding='utf-8') as f:
            json.dump(data, f, indent=2, ensure_ascii=False)
            f.write('\n')
        print(f'✓ {lang}.json — added {added_for_lang} keys')
        added_total += added_for_lang
    else:
        print(f'  {lang}.json — already up to date')

print(f'\nDone. Added {added_total} translations across {len(TRANSLATIONS)} locales.')
