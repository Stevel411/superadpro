# Apply each translation set to its locale file
for lang, translations in TRANSLATIONS.items():
    path = LOCALES_DIR / f'{lang}.json'
    if not path.exists():
        print(f'! {lang}.json missing, skipping')
        continue
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    if 'commandCentre' in data:
        print(f'  {lang}.json already has commandCentre, skipping')
        continue
    data['commandCentre'] = translations
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
        f.write('\n')
    print(f'✓ {lang}.json — added {len(translations)} keys')

print(f'\nDone. Processed {len(TRANSLATIONS)} non-English locales.')
